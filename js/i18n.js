(function () {
    'use strict';

    const STORAGE_KEY = 'khusiyal-lang-v2';

    function getStoredLang() {
        try {
            return localStorage.getItem(STORAGE_KEY) || 'en';
        } catch (e) {
            return 'en';
        }
    }

    function setStoredLang(lang) {
        try {
            localStorage.setItem(STORAGE_KEY, lang);
        } catch (e) {
            // localStorage not available
        }
    }

    function applyTranslations(lang) {
        const dict = translations[lang];
        if (!dict) return;

        // Translate text content
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (dict[key] !== undefined) {
                // For elements with icons inside, preserve the icon
                const icon = el.querySelector('i, svg');
                if (icon && el.children.length <= 1) {
                    el.textContent = '';
                    el.appendChild(icon);
                    el.append(' ' + dict[key]);
                } else if (el.childElementCount === 0) {
                    el.textContent = dict[key];
                } else {
                    // Complex inner HTML — only update text nodes
                    const walker = document.createTreeWalker(el, NodeFilter.SHOW_TEXT);
                    const textNodes = [];
                    while (walker.nextNode()) textNodes.push(walker.currentNode);
                    if (textNodes.length === 1) {
                        textNodes[0].textContent = dict[key];
                    } else if (textNodes.length > 1) {
                        // Find the main text node (longest one)
                        const main = textNodes.reduce((a, b) =>
                            a.textContent.trim().length >= b.textContent.trim().length ? a : b
                        );
                        main.textContent = ' ' + dict[key];
                    }
                }
            }
        });

        // Translate placeholders
        document.querySelectorAll('[data-i18n-ph]').forEach(el => {
            const key = el.getAttribute('data-i18n-ph');
            if (dict[key] !== undefined) {
                el.setAttribute('placeholder', dict[key]);
            }
        });

        // Update html lang attribute
        document.documentElement.lang = lang === 'od' ? 'or' : 'en';

        // Update toggle switch state
        document.querySelectorAll('.lang-switch').forEach(sw => {
            sw.querySelectorAll('.lang-label').forEach(lbl => {
                lbl.classList.toggle('active', lbl.getAttribute('data-lang') === lang);
            });
        });
    }

    function initLanguageToggle() {
        const lang = getStoredLang();

        // Apply saved language on load
        applyTranslations(lang);

        // Attach click handler — toggle on whole button click
        document.querySelectorAll('button.lang-switch').forEach(btn => {
            btn.addEventListener('click', () => {
                const currentLang = getStoredLang();
                const newLang = currentLang === 'en' ? 'od' : 'en';
                setStoredLang(newLang);
                applyTranslations(newLang);
            });
        });
    }

    // Initialize when DOM is ready
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initLanguageToggle);
    } else {
        initLanguageToggle();
    }
})();
